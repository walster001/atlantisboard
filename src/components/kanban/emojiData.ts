// Constants
export const RECENT_EMOJIS_KEY = 'toastui-recent-emojis';
export const MAX_RECENT_EMOJIS = 24;

// Helper functions
export function getRecentEmojis(): string[] {
  try {
    const stored = localStorage.getItem(RECENT_EMOJIS_KEY);
    return stored ? JSON.parse(stored) : [];
  } catch {
    return [];
  }
}

export function addRecentEmoji(emoji: string): void {
  try {
    let recent = getRecentEmojis();
    recent = [emoji, ...recent.filter(e => e !== emoji)].slice(0, MAX_RECENT_EMOJIS);
    localStorage.setItem(RECENT_EMOJIS_KEY, JSON.stringify(recent));
  } catch {
    // Ignore storage errors
  }
}

// Build searchable emoji list
export function getAllEmojis(): Array<{ emoji: string; category: string }> {
  const result: Array<{ emoji: string; category: string }> = [];
  for (const [category, data] of Object.entries(EMOJI_CATEGORIES)) {
    for (const emoji of data.emojis) {
      result.push({ emoji, category });
    }
  }
  return result;
}

